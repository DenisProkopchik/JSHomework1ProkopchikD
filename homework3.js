/*Игра «Угадай число». Предложить пользователю загадать
число от 0 до 100 и отгадать его следующим способом:
каждую итерацию цикла делите диапазон чисел пополам,
записываете результат в N и спрашиваете у пользователя
«Ваше число > N, < N или == N?». В зависимости от того
что указал пользователь, уменьшаете диапазон. Начальный
диапазон от 0 до 100, поделили пополам и получили 50.
Если пользователь указал, что его число > 50, то изменили
диапазон на от 51 до 100. И так до тех пор, пока пользова-
тель не выберет == N.*/

let numAnswer = 50;
let numFind = numAnswer;
let numDivid10 = false;

alert('Загадайте число от 0 до 100');
while (numDivid10 === false) {
  numDivid10 = prompt(`Ваше число больше ">", меньше "<" или равно "=" ${numAnswer}?`);
  numFind = numFind / 2;
  switch (numDivid10) {
    case ">":
      numAnswer = Math.round(numAnswer + numFind);
      numDivid10 = false;
      break;
    case "<":
      numAnswer = Math.round(numAnswer - numFind);
      numDivid10 = false;
      break;
    case "=":
      numDivid10 = true;
      break;
    default:
      numFind = numFind * 2;
      alert("Вы ввели не правильное значение знака. Введите математический знак >, <, или =.");
      numDivid10 = false;
  }
}
alert(`Мы отгадали ваше число, оно равно ${numAnswer}`);