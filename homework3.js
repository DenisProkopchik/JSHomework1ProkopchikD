//Задача  6
/*Зациклить калькулятор. Запросить у пользователя 2 числа
и знак, решить пример, вывести результат и спросить, хо-
чет ли он решить еще один пример. И так до тех пор, пока
пользователь не откажется.*/

let question = true;

while (question === true) {
  let num61 = +prompt('Введите первое число');
  let sign = prompt('Введите математический знак +, -, / или *.');
  let num62 = +prompt('Введите второе число');
  let result = 0;

  if (!num61 || !num62 || !sign) {
    alert("Это не выполнится");
    break;
  }
  switch (sign) {
    case "+":
      result = num61 + num62;
      question = confirm(`${num61} + ${num62} = ${result} ваш пример. Вы хотите решить еще один пример?`);
      break;
    case "-":
      result = num61 - num62;
      question = confirm(`${num61} - ${num62} = ${result} ваш пример. Вы хотите решить еще один пример?`);
      break;
    case "*":
      result = num61 * num62;
      question = confirm(`${num61} * ${num62} = ${result} ваш пример. Вы хотите решить еще один пример?`);
      break;
    case "/":
      result = num61 / num62;
      question = confirm(`${num61} / ${num62} = ${result} ваш пример. Вы хотите решить еще один пример?`);
      break;
    default:
      question = confirm("Вы ввели не правильное значение знака. Введите математический знак +, -, / или *.");
  }
}

